package com.glue.struct;

/**
 * An interface representing an invited participant.
 * 
 */

public interface IInvitedParticipant {

	/**
	 * Returns the name of the invited participant.
	 * 
	 * @return the name of the invited participant.
	 */
	String getName();

	/**
	 * Returns the invited participant email.
	 * 
	 * @return the invited participant email.
	 */
	String getMail();

}
